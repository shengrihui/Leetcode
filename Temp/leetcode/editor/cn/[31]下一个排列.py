        if i >= 0:
            l, r = i + 1, n - 1
            while l <= r:
                mid = (l + r) // 2
                if nums[mid] <= nums[i]:
                    r = mid - 1
                else:
                    l = mid + 1
            nums[r], nums[i] = nums[i], nums[r]
