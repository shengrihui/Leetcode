class Solution:
    def successfulPairs(self, spells: List[int], potions: List[int], success: int) -> List[int]:
        n, m, l = len(spells), len(potions), max(potions) + 1
        suf = [0] * l  # suf[i] åœ¨ potions é‡Œå¤§äºŽç­‰äºŽ i çš„æœ‰å‡ ä¸ª
        for x in potions:
            suf[x] += 1
        for i in range(1, l):
            suf[-i - 1] += suf[-i]
        ans = []
        suc = success - 1
        for x in spells:
            y = suc // x + 1
            ans.append(suf[y] if y < l else 0)
        return ans


