class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        # if not root:
        #     return 0
        # else:
        #     return 1 + max(self.maxDepth(root.left), self.maxDepth(root.right))
        if not root:
            return 0
        depth = 0
        q = deque()
        q.append((root, depth + 1))
        while q:
            node, depth = q.popleft()
            if node.left:
                q.append((node.left, depth + 1))
            if node.right:
                q.append((node.right, depth + 1))
        return depth
