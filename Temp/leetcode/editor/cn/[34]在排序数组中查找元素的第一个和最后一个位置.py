class Solution:
    def searchRange(self, nums: List[int], target: int) -> List[int]:
        n = len(nums)

        def bi_search(target):
            l, r = 0, n - 1
            while l <= r:
                mid = (l + r) // 2
                if nums[mid] >= target:
                    r = mid - 1
                else:
                    l = mid + 1
            return l

        start = bi_search(target)
        if start == n or nums[start] != target:
            return [-1, -1]
        end = bi_search(target + 1) - 1
        return [start, end]

