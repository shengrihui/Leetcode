class Solution:
    def mergeKLists(self, lists: List[Optional[ListNode]]) -> Optional[ListNode]:
        q = [node for node in lists if node is not None]
        heapq.heapify(q)
        head = cur = ListNode(-1)
        while q:
            node = heapq.heappop(q)
            cur.next = node
            if node.next is not None:
                heapq.heappush(q, node.next)
            cur = cur.next
        return head.next

