class Solution:
    def findMin(self, nums: List[int]) -> int:
        n = len(nums)
        if n == 1 or nums[0] < nums[-1]:
            return nums[0]
        l, r = 0, n - 1
        while l < r:  # [l,r)  [0,n-1)
            mid = (l + r) // 2
            # æ¯æ¬¡éƒ½ä¸åŸæ•°ç»„æœ€å³è¾¹çš„æ¯”è¾ƒï¼Œ
            # å¦‚æœå°äºç­‰äºï¼Œåˆ™è¯´æ˜æœ€å°å€¼åœ¨ [left,mid)
            # ç­‰äºæ˜¯åªæœ‰ä¸€ä¸ªå…ƒç´ çš„æ—¶å€™
            # æ¯æ¬¡ä¸ä¸€ä¸ªç¡®å®šäº†çš„æ•°æ¯”å¤§å°
            if nums[mid] <= nums[r]:
                r = mid
            else:
                l = mid + 1
        return nums[l]

